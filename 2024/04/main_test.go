func TestSearch(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  18,
		},
		{
			name:  "actual",
			input: input,
			want:  2517,
		},
	}
	for _, tt := range tests {
		shirt := NewShirt(tt.input)
		t.Run(tt.name, func(t *testing.T) {
			if got := shirt.Search(); got != tt.want {
				t.Errorf("calculate = %v\n%v\nwant: %v\n%v", got, shirt.String(), tt.want, tt.input)
			}
		})
	}
}

