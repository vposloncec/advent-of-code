func TestAntiNodes(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  14,
		},
		{
			name:  "actual",
			input: input,
			want:  305,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			f := NewAntiNodeFinder(tt.input)
			if got := f.FindAntiNodes(); got != tt.want {
				t.Errorf("calculate = %v\n\nwant: %v, map: \n", got, tt.want)
			}
		})
	}
}

