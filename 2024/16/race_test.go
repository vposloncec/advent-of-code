func TestRace(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  7036,
		},
		{
			name:  "example2",
			input: example2,
			want:  11048,
		},
		{
			name:  "actual",
			input: input,
			want:  135536,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			r := NewRace(tt.input)
			if got := r.Calculate(); got != tt.want {
				t.Errorf("calculate = %v\n\nwant: %v, map: \n", got, tt.want)
			}
		})
	}
}

